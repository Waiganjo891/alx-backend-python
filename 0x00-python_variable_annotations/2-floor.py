#!/usr/bin/env python3
"""
Module 2-floor
This module provides a function to compute the floor of a given float.
"""
import math


def floor(n: float) -> int:
    """
    Returns the floor of the given float.
    Parameters:
    n (float): The float to compute the floor of.
    Returns:
    int: The floor of the float.
    """
    return math.floor(n)

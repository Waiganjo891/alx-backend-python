#!/usr/bin/env python3
"""
Module 3-to_str
This module provides a function to convert a float to its string representation.
"""


def to_str(n: float) -> str:
    """
    Returns the string representation of the given float.
    Parameters:
    n (float): The float to convert to a string.
    Returns:
    str: The string representation of the float.
    """
    return str(n)
